"use client";

import { useEffect, useState } from "react";
import Link from "next/link";
import { Nav } from "@/components/ui/Nav";
import { Footer } from "@/components/ui/Footer";
import { ButtonLink } from "@/components/ui/Button";
import { ReportViewer } from "@/components/results/ReportViewer";

interface SubmissionSummary {
  companyA: string;
  companyB: string;
  submittedAt: string;
}

export default function ResultsPage() {
  const [summary, setSummary] = useState<SubmissionSummary | null>(null);
  const [ready, setReady] = useState(false);

  useEffect(() => {
    try {
      const raw = window.localStorage.getItem("gb-submission");
      if (raw) setSummary(JSON.parse(raw) as SubmissionSummary);
    } catch {
      /* ignore */
    }
    setReady(true);
  }, []);

  return (
    <>
      <Nav />
      <main className="mx-auto max-w-5xl px-6 py-10">
        <div className="mb-8 flex flex-wrap items-end justify-between gap-4">
          <div>
            <p className="text-sm font-medium text-gold-600 dark:text-gold-300">
              Compatibility Report
            </p>
            <h1 className="font-display text-3xl font-semibold tracking-tight text-navy-900 dark:text-white">
              Report
            </h1>
            <p className="mt-1 text-sm text-charcoal-400">
              {summary
                ? `Analysis for ${summary.companyA} × ${summary.companyB}, prepared ${new Date(
                    summary.submittedAt
                  ).toLocaleDateString()}.`
                : "Upload a report, or start a new analysis to organize company data first."}
            </p>
          </div>
          <ButtonLink href="/analyze" variant="outline">
            New Analysis
          </ButtonLink>
        </div>

        {ready && (
          <ReportViewer companyA={summary?.companyA} companyB={summary?.companyB} />
        )}

        {ready && !summary && (
          <p className="mt-6 text-center text-sm text-charcoal-400">
            No analysis on file yet.{" "}
            <Link href="/analyze" className="font-medium text-gold-600 underline dark:text-gold-300">
              Start one →
            </Link>
          </p>
        )}

        <p className="mt-10 text-center text-xs text-charcoal-400">
          Reports are generated by the ma-compatibility-scoring skill for evaluation purposes and do
          not constitute investment, legal, or tax advice.
        </p>
      </main>
      <Footer />
    </>
  );
}
